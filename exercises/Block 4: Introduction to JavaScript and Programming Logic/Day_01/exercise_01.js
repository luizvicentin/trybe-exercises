console.log('A multiplicação de a e b é igual a ' + multi + '.');

let div = a / b;
console.log('A divisão entre a e b é igual a ' + div + '.');

let mod = a % b;
console.log('O módulo entre a e b dá o resto igual a ' + mod + '.');
